import math

# Mendapatkan nilai minimum dan maksimum
x = min(5, 10, 25)
y = max(6, 12, 18)
print("(x,y)")
print(f"({x},{y})")

# Mendapatkan nilai absolut
x = abs(-7.25)
print(x)

# Medapatkan nilai pangkat
x = math.pow(4, 3)
print(x)

# Mendapatkan nilai akar
x = math.sqrt(64)
print(x)

# Mendapatkan nilai pembulatan
x = math.ceil(1.4)
print(x)
x = math.floor(1.4)
print(x)

